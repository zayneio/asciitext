function buildAsciiArtFromString(input) {
  // Normalize the input to lowercase for matching dictionary keys
  input = input.toLowerCase();

  // Split the input into lines for each ASCII character part
  let asciiLines = ['', '', '', '', '', '', ''];

  // Build up each line of the ASCII art
  for (let char of input) {
    if (asciiArtDict[char]) {
      const charLines = asciiArtDict[char].split('\n');
      for (let i = 0; i < asciiLines.length; i++) {
        // Append each line of the ASCII char to the corresponding line of the ASCII art
        asciiLines[i] += charLines[i] || '';
      }
    }
  }

  // Join all the lines into a single string
  return asciiLines.join('\n');
}

